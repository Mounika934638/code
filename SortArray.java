import java.util.Arrays;

public class SortArray {
    public static void main(String[] args) {
        // Example array
        int[] arr = {5, 2, 8, 1, 3};

        // Sorting the array using Arrays.sort() method
        Arrays.sort(arr);

        // Printing the sorted array
        System.out.println("Sorted array: " + Arrays.toString(arr));
    }
}
